#!/usr/bin/env node
// Bundle pkpass assets into a TypeScript module so the Cloudflare Worker can
// read them without hitting the filesystem. Run this after gen-pkpass-assets.mjs
// any time the assets change.

import { readFileSync, readdirSync, writeFileSync } from 'node:fs'
import { resolve, dirname } from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const ROOT = resolve(__dirname, '..')
const ASSETS_DIR = resolve(ROOT, 'assets/pkpass')
const OUT_FILE = resolve(ROOT, 'src/lib/pkpass-assets.generated.ts')

const files = readdirSync(ASSETS_DIR).filter(f => f.endsWith('.png')).sort()

const entries = files.map(f => {
  const b64 = readFileSync(resolve(ASSETS_DIR, f)).toString('base64')
  return `  ${JSON.stringify(f)}: ${JSON.stringify(b64)},`
}).join('\n')

const content = `// AUTO-GENERATED by scripts/gen-pkpass-asset-module.mjs — do not edit by hand.
// Bundles pkpass image assets as base64 so the Worker can pass Buffers to
// passkit-generator without touching the filesystem.

const ASSETS_BASE64: Record<string, string> = {
${entries}
}

export function loadPkpassAssets(): Record<string, Buffer> {
  const out: Record<string, Buffer> = {}
  for (const [name, b64] of Object.entries(ASSETS_BASE64)) {
    out[name] = Buffer.from(b64, 'base64')
  }
  return out
}
`

writeFileSync(OUT_FILE, content)
console.log(`wrote ${OUT_FILE}`)
console.log(`bundled ${files.length} assets: ${files.join(', ')}`)
